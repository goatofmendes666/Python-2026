# This is a simple project of a simple Calculator.
# In this we perform a simple calculations on 2 variables
# Add, Subtract, Multiply, Divide, Modulus, Floor Division.
a = 15
b = 7
add = a + b
print("The sum of", a, "and", b, "is -->", add)
sub = a - b
print("The Subtraction  of", a, "and", b, "is -->", sub)
mul = a * b
print("The Multiplication  of", a, "and", b, "is -->", mul)
div = a / b
print("The Division  of", a, "and", b, "is -->", div)
mod = a % b
print("The Modulus  of", a, "and", b, "is -->", mod)
fdiv = a // b
print("The Floor Division  of", a, "and", b, "is -->", fdiv)
